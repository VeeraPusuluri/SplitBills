@Composable
fun MyApplicationTheme(
    darkTheme: Boolean = isSystemInDarkTheme(),
    content: @Composable () -> Unit
) {
    val colors = if (darkTheme) {
        darkColors(
            primary = Color(0xFFBB86FC),
            primaryVariant = Color(0xFF3700B3),
            secondary = Color(0xFF03DAC5)
        )
    } else {
        lightColors(
            primary = Color(0xFF6200EE),
            primaryVariant = Color(0xFF3700B3),
            secondary = Color(0xFF03DAC5)
        )
    }
    val typography = Typography(
        body1 = TextStyle(
            fontFamily = FontFamily.Default,
            fontWeight = FontWeight.Normal,
            fontSize = 16.sp
        )
    )
    val shapes = Shapes(
        small = RoundedCornerShape(4.dp),
        medium = RoundedCornerShape(4.dp),
        large = RoundedCornerShape(0.dp)
    )

    MaterialTheme(
        colors = colors,
        typography = typography,
        shapes = shapes,
        content = content
    )
}

class MainActivity : ComponentActivity() {
    override fun onCreate(savedInstanceState: Bundle?) {
        super.onCreate(savedInstanceState)
        setContent {
            MyApplicationTheme {
                Surface(
                    modifier = Modifier.fillMaxSize(),
                    color = MaterialTheme.colors.onSecondary,
                ) {
                    SplashScreen()
                }
            }
        }
        WindowCompat.setDecorFitsSystemWindows(this.window,false)
    }
}



@Composable
fun SplashScreen() {
    val imageSplashScreen = painterResource(id = R.drawable.lemon_tree)
    Column(modifier = Modifier
        .fillMaxWidth()
        .fillMaxHeight()) {
        Image(painter = imageSplashScreen, contentDescription = "tree with lemons Image",
            modifier = Modifier.fillMaxHeight().fillMaxWidth().border(width = 1.dp, color = Color.White), contentScale = ContentScale.Fit)
    }
}

